package classwork.chapter3P1;

public class SimpleTypes {

    public static void main(String[] args) {


        //օրինակ 1
        //թվեր պահելու պարզ տիպերը
        byte b = 111;
        System.out.println("b = " + b);

        short s = 30000;
        System.out.println("s = " + s);

        int i = 100000000;
        System.out.println("i = " + i);

        long l = 14375472;
        System.out.println("l = " + l);


        //օրինակ 2
        //սինվոլ պահելու տիպ

        char c = '/';
        char c1 = '9';
        char c2 = 'a';
        System.out.println("c = " + c + "c1 = " + c1 + "c2 = " +c2 );

        //օրինակ 3
        //ամբօղջ թվեր պահելու տիպեր

        float f = 45.33f;
        double d = 4663.46;

        System.out.println("f = " + f);
        System.out.println("d = " + d);

        //օրինակ 4
        //true կամ  false պահելու տիպ
        boolean b1  = false;
        boolean b3 = true;
        System.out.println("b1 = " +b1);
        System.out.println("b3 = " +b3);
    }
}
